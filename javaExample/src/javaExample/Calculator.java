package javaExample;

public class Calculator {
	int getAdd(int num1, int num2) {
		return num1+num2;
	}
	
	int getSub(int num1, int num2) {
		return num1-num2;
	}
	
	int getMul(int num1, int num2) {
		return num1*num2;
	}
	
	float getDiv(int num1, int num2) {
		return num1/(float)num2;
	}
	
	
}
